// Merge base project with Russian overrides (if any).
export function getProject(projects, slug, lang) {
  const base = projects.find((p) => p.slug === slug);
  if (!base) return null;
  if (lang === 'ru' && RU_PROJECTS[slug]) {
    return { ...base, ...RU_PROJECTS[slug] };
  }
  return base;
}
